let number = 1;

console.log(number++); // 1: сначала выводит number, затем увеличивает его на 1 (number становится 2)
console.log(++number); // 3: сначала увеличивает number на 1 (number становится 3), затем выводит его
console.log(number--); // 3: сначала выводит number, затем уменьшает его на 1 (number становится 2)
console.log(--number); // 1: сначала уменьшает number на 1 (number становится 1), затем выводит его
